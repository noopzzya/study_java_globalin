package study;

import java.io.*;

public class Test01 {

	public static void main(String[] args) {
		
		//변수선언
		//자료형 + 변수명
		int a = 100;
		// a라는 변수에 100을 대입하라
		
		System.out.println("100+100"); //더블쿼터 (" ") 있으면 문자열로 인식
		System.out.println(100+100); //더블쿼터 (" ") 없으면 수치로 인식
		System.out.println(a);
		
		System.out.println("연습");
		
		System.out.printf("100+100");
		System.out.printf("\n");
		System.out.printf("%d",100+100);
		System.out.printf("\n");
		System.out.printf("%d,%d", 200,300);

		
		
	}

}
